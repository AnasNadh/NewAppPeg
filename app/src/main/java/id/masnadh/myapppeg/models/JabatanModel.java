package id.masnadh.myapppeg.models;

public class JabatanModel {
    String jabatan, eselon, tmt_jabatan, status_jab,id_jab;
    int id;

    public JabatanModel() {
        this.jabatan = jabatan;
        this.eselon = eselon;
        this.tmt_jabatan = tmt_jabatan;
        this.status_jab = status_jab;
        this.id = id;
        this.id_jab = id_jab;
    }

    public String getJabatan() {
        return jabatan;
    }

    public void setJabatan(String jabatan) {
        this.jabatan = jabatan;
    }

    public String getEselon() {
        return eselon;
    }

    public void setEselon(String eselon) {
        this.eselon = eselon;
    }

    public String getTmt_jabatan() {
        return tmt_jabatan;
    }

    public void setTmt_jabatan(String tmt_jabatan) {
        this.tmt_jabatan = tmt_jabatan;
    }

    public String getStatus_jab() {
        return status_jab;
    }

    public void setStatus_jab(String status_jab) {
        this.status_jab = status_jab;
    }

    public String getId_jab() {
        return id_jab;
    }

    public void setId_jab(String id_jab) {
        this.id_jab = id_jab;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
